#include "libft.h"

int	ft_isfloat(char *c)
{
	if (*c == '-')
		c++;
	if (ft_isdigit(*c))
		c++;
	if (*c == '.')
		c++;
	while (*c)
	{
		if (ft_isdigit(*c))
			c++;
		else
			return (0);
	}
	return (1);
}
